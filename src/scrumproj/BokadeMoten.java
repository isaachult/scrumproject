    @Override
    public void updateInfo() { 
          
        try {
            DefaultTableModel table = new DefaultTableModel(new Object[]{"BESKRIVNING", "DATUM", "TID"}, 0);

            ArrayList<HashMap<String, String>> allaRader;
            int inloggadId = app.getCurrentUser();
             allaRader = app.getDataBaseConnection().fetchRows("SELECT BESKRIVNING, DATUM, TID FROM MOTEN JOIN BOKADE_MOTEN ON MOTEN.MOTES_ID = BOKADE_MOTEN.MOTES_ID and BOKADE_MOTEN.ANVANDAR_ID = '"+inloggadId+"'");

            for (HashMap<String, String> rad : allaRader) {
                String beskrivning = rad.get("BESKRIVNING");
                String datum = rad.get("DATUM");
                String tid = rad.get("TID");
                

                //Sätter in anvandare + inlagg + datum  i tabellen mha addRow()
                table.addRow(new Object[]{beskrivning, datum, tid});
                
            }

            //Säger att tblInlagg ska använda sig av den modellen vi skapade ovan
            tblMoten.setModel(table);

            //Sätter defaultEditor till Object.class, null för att data i tabellen inte ska kunna redigeras, men kunna selekteras.
            tblMoten.setDefaultEditor(Object.class, null);

            //Stänger av möjligheten att ändra kolumnordningen.
            tblMoten.getTableHeader().setReorderingAllowed(false);

            //Sätter bredden på kolumnen som innehåller ID till 0px.
            //Vi vill inte att ID ska visas i listan men vill eventueltl kunna använda IDt 
            //tblMoten.getColumnModel().getColumn(3).setMinWidth(0);
            //tblMoten.getColumnModel().getColumn(3).setMaxWidth(0);
            //tblMoten.getColumnModel().getColumn(3).setWidth(0);

        } catch (InfException e) {
            JOptionPane.showMessageDialog(null, "Något gick fel!");
            System.err.println(e);
        }
        catch (NullPointerException e) {
            System.out.println("Du har inga bokade möten!");
            System.err.println(e.getMessage());
        }
    }

